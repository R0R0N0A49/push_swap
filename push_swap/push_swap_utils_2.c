#include "push_swap.h"

void	ft_loop(char **rsl, char *s2, int *i, int *j)
{
	if (s2[0] == ' ' && (s2[1] >= '0' && s2[1] <= '9'))
	{
		i[0]--;
		j[0]++;
	}
	while (s2 && s2[j[0]])
	{
		if (s2[j[0]] == ' ' && s2[j[0] + 1] == ' ')
		{
			i[0]--;
			j[0]++;
		}
		else
			rsl[0][i[0] + j[0]] = s2[j[0]];
		j[0]++;
	}
}
